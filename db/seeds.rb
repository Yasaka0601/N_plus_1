# ================================
#  トレーナー配列（10人）
# ================================
TRAINER_NAMES = [
  "サトシ", "レッド", "グリーン", "ブルー", "カスミ",
  "タケシ", "シゲル", "ワタル", "サカキ", "ロケットだん"
]

puts "データをリセット中"
Pokemon.delete_all
Trainer.delete_all

puts "トレーナーとポケモンを作成中"

TRAINER_NAMES.each do |name|
  trainer = Trainer.create!(
    name: name,
    email: "#{name}@example.com",
    password: "password"
  )

  POKEMON.sample(6).each do |pokemon_name|
    Pokemon.create!(
      name: pokemon_name,
      trainer: trainer
    )
  end
end

puts "作成が完了しました！"
puts "（合計 #{Trainer.count} 人のトレーナー、#{Pokemon.count} 匹のポケモン）"